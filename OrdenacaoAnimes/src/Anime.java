class Anime {
    String titulo;
    String genero;
    int numEpisodios;
    double avaliacao;

    public Anime(String titulo, String genero, int numEpisodios, double avaliacao) {
        this.titulo = titulo;
        this.genero = genero;
        this.numEpisodios = numEpisodios;
        this.avaliacao = avaliacao;
    }
}